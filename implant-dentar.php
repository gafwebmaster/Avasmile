        <h2>Ce este un implant dentar?</h2>
        <p>Un implant dentar este utilizat pentru a înlocui rădăcina unuia sau mai multor dinți lipsă pe arcada superioară sau inferioară a maxilarului. Implanturile dentare sunt plasate în osul alveolar (sub gingii) al maxilarului pentru a găzdui un aparat dentar fix care înlocuiește dinții.</p>
        <p>Implanturile dentare sunt realizate din titan. Acest material este foarte rezistent, asigură o durată de viață îndelungată a implantului dentar .</p>
