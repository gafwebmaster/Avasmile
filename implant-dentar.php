        <h2>De ce să alegeți un implant dentar?</h2>
        <p>Există mai multe motive pentru a alege un implant dentar. În primul rând, lipsa unuia sau a mai multor dinți poate cauza probleme in masticatie. Un implant dentar poate face acest lucru mai confortabil și mai eficient. În plus, montarea unui implant dentar previne deplasarea dinților din jurul unui dinte lipsă. De asemenea, previne pierderea osoasă cauzată de un dinte lipsă.</p>
        <p>În cele din urmă, un implant dentar poate reda încrederea pacienților, oferindu-le un zâmbet frumos.</p>


        <h2>Care este procesul de montare a unui implant dentar?</h2>
        <p>Montarea unui implant dentar este realizată de un medic specialist in chirurgie maxilo-faciala. Procesul în sine nu este dureros pentru pacient, deoarece se va face sub anestezie locală, cu sau fără sedare intravenoasă pentru a-i ajuta pe pacienții mai anxioși.</p>
        <p>În primă fază se efectuează o examinare orală amănunțită pentru a pregăti pacientul pentru plasarea implantului și pentru a determina ce tip de implant este cel mai potrivit. Apoi, chirurgul va începe montarea implantului dentar, o procedură scurtă și neinvazivă. Apoi, după ce țesutul gingival s-a vindecat, chirurgul testează rezistența implantului. Dacă totul arată bine, dispozitivul protetic poate fi în cele din urmă plasat.</p>


        <h2>Există contraindicații pentru această procedură?</h2>
        <p>Primul factor care ar putea face ca plasarea unui implant să fie nerecomandată este faptul că osul maxilarului este în stare precara. Dacă osul maxilarului s-a deteriorat și nu este suficient, implantul nu poate fi plasat. Cu toate acestea, în unele cazuri, o grefă osoasă poate fi folosită pentru a remedia această problemă.</p>
